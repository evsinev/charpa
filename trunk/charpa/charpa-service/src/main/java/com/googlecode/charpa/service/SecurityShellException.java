package com.googlecode.charpa.service;

/**
 * SecurityShell exception
 */
public class SecurityShellException extends Exception {
    /**
     * {@inheritDoc}
     */
    public SecurityShellException(String message) {
        super(message);
    }

    /**
     * {@inheritDoc}
     */
    public SecurityShellException(String message, Throwable cause) {
        super(message, cause);
    }
}
